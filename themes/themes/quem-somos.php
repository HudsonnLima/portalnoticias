<strong>TR Revista</strong> surgiu em março de 2013 com o propósito de ser um órgão de informação independente. <br>
Contrariando opiniões sobre a impossibilidade de existir imparcialidade na imprensa do interior, o objetivo era noticiar fatos ocorridos em Três Rios e cidades da região, tendo como público-alvo a  comunidade, com suas histórias, vitórias, reivindicações, informações para facilitar o dia-a-dia, os problemas da cidade,  com um texto leve e descontraído, e  de fácil entendimento. <br>
Para justificar o termo “revista”, aliamos ao noticiário um  conteúdo variado, formado por crônicas, artigos, colunas, para ser  utilizado por vários dias,  sem perder a atualidade.
De lá para cá, muita coisa aconteceu. O que deveria ser um blog se transformou num site, passou por várias transformações, esteve fora do ar algumas vezes por motivos técnicos, e agora chegamos ao formato  que esperamos ser  duradouro. Cara nova, ideias “velhas”, os mesmos objetivos: prestar serviço, fazer imprensa com  responsabilidade, com  INDEPENDÊNCIA,  aliada, sempre, à VERDADE. Estes são os princípios que norteiam o jornalismo sério, objetivo principal do TR Revista.<br><br>

